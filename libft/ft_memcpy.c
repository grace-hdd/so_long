#include "libft.h"

void	*ft_memcpy(void *dest, const void *src, int n)
{
	char		*d;
	const char	*s;
	int			i;

	if (!dest && !src)
		return (0);
	d = (char *)dest;
	s = (const char *)src;
	i = 0;
	while (i < n)
	{
		d[i] = s[i];
		i++;
	}
	return (dest);
}
